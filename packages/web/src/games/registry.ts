// Game registry — single source of truth for the games surfaced in the UI.
//
// The catalog (every browse-able game) lives in `catalog.json` and is the
// authority for slug, BGG id, accent hex, family membership, and any
// per-entry editorial overrides (`displayTitle`, `bggOverrides`). Playable
// games additionally export a `PlayableModule` from `<slug>/index.ts`
// holding the React component, mode, tournament strategies, etc. — the
// presence of that file flips a slug from `kind: "catalog"` to
// `kind: "playable"`.
//
// Bootstrapping order:
//   1. Validate catalog.json through `CatalogSchema` (loud failure on bad data).
//   2. Glob every `<slug>/index.ts` — the matches are the playable set.
//   3. Glob `<slug>/descriptions.generated.ts` and thumbnail.webp by path.
//   4. For each catalog entry, hydrate with BGG + thumbnail + descriptions
//      and — if a playable module is registered for the slug — merge in its
//      extras and tag with `kind: "playable"`. Catalog-only entries get
//      `kind: "catalog"`.
//
// Failures we treat as hard errors at module load (not silently dropped):
//   - catalog.json fails schema validation
//   - a playable `index.ts` has no matching catalog entry
//   - a catalog entry has no BGG snapshot row
//
// Soft failures (still surface a `GameDefinition`, just with a fallback):
//   - missing thumbnail → shared placeholder svg
//   - missing descriptions.generated.ts → repeat `bgg.description`

import { bggSnapshot } from "@boardgames/core/bgg";
import { CATALOG as catalogRaw } from "@boardgames/core/games/catalog";
import placeholderThumbnail from "./_placeholder-thumbnail.svg?url";
import { CatalogSchema } from "./catalog-schema";
import type {
  CatalogEntry,
  CatalogGame,
  GameDefinition,
  GameDescriptions,
  PlayableGame,
  PlayableModule,
} from "./types";

// ── Catalog (eager, validated) ────────────────────────────────────────

/**
 * Loud-failure validation at module load. catalog.json is a small,
 * committed file edited by humans — a bad commit should crash the build,
 * not surface as a confused "missing game" hours later.
 */
const catalog: CatalogEntry[] = CatalogSchema.parse(catalogRaw);
const catalogBySlug = new Map(catalog.map((c) => [c.slug, c]));

// ── Playable modules glob ────────────────────────────────────────────

/**
 * Eager glob of every `<slug>/index.ts` that exists on disk. After the
 * Fix-5 migration these are *only* the playable games (~8 of them).
 * Catalog-only entries no longer have an index.ts — their data lives
 * entirely in catalog.json.
 */
const playableModules = import.meta.glob<{ default: PlayableModule }>("./*/index.ts", {
  eager: true,
});

function slugFromModulePath(path: string): string {
  const match = path.match(/^\.\/([^/]+)\/index\.ts$/);
  if (!match) throw new Error(`registry: unexpected module path "${path}"`);
  return match[1];
}

/** Map of slug → playable extras. Built once at module init. */
const playableBySlug = new Map<string, PlayableModule>();
for (const [path, mod] of Object.entries(playableModules)) {
  const slug = slugFromModulePath(path);
  if (!catalogBySlug.has(slug)) {
    throw new Error(
      `[registry] Playable module "${slug}" has no catalog entry. Add it to packages/core/src/games/catalog.json or delete the index.ts.`,
    );
  }
  playableBySlug.set(slug, mod.default);
}

// ── Side-data globs ─────────────────────────────────────────────────

/**
 * Per-game generated descriptions. Emitted by
 * `pnpm gen-descriptions`. Absent for homebrew games (bggId: 0) and any
 * game that hasn't been run through the pipeline yet — those fall back
 * to `bgg.description` repeated across all three slots.
 */
const descriptionModules = import.meta.glob<{ descriptions: GameDescriptions }>(
  "./*/descriptions.generated.ts",
  { eager: true },
);

const descriptionsBySlug: Record<string, GameDescriptions> = {};
for (const [path, mod] of Object.entries(descriptionModules)) {
  const m = path.match(/^\.\/([^/]+)\/descriptions\.generated\.ts$/);
  if (m) descriptionsBySlug[m[1]] = mod.descriptions;
}

/**
 * Collect every game's thumbnail URL. Generated by `pnpm thumbnails`
 * (scripts/optimize-thumbnails.mjs) from the original full-resolution PNGs.
 * Pandemic ships at `assets/img/thumbnail.webp`; everyone else uses
 * `assets/thumbnail.webp`.
 */
const thumbnailModules = import.meta.glob<string>(
  ["./*/assets/thumbnail.webp", "./*/assets/img/thumbnail.webp"],
  { eager: true, query: "?url", import: "default" },
);

const thumbnailBySlug: Record<string, string> = {};
for (const [path, url] of Object.entries(thumbnailModules)) {
  const m = path.match(/^\.\/([^/]+)\/assets\//);
  if (m) thumbnailBySlug[m[1]] = url;
}

// ── Hydration ────────────────────────────────────────────────────────

/**
 * Build the resolved `GameDefinition` for one catalog entry. Discriminator
 * (`kind`) is set based on whether a playable module is registered for the
 * slug.
 */
function buildDefinition(entry: CatalogEntry): GameDefinition {
  const snapshotEntry = bggSnapshot[entry.slug];
  if (!snapshotEntry) {
    throw new Error(
      `[registry] No BGG snapshot entry for slug "${entry.slug}". Run \`pnpm bgg-sync\` to refresh packages/core/src/bgg/snapshot.json.`,
    );
  }

  // Three-length description resolution. Precedence (highest wins):
  //   1. `bggOverrides.description` — per-entry manual escape hatch.
  //      Applies to all three variants uniformly (the human edit is
  //      authoritative).
  //   2. Generated `descriptions.generated.ts` — from the gen-descriptions
  //      pipeline; each variant char-budgeted to its target surface.
  //   3. `bgg.description` — raw BGG fallback, repeated. Ensures every
  //      game ships *something* even without a generated file.
  const bgg = { ...snapshotEntry, ...(entry.bggOverrides ?? {}) };
  const manualOverride = entry.bggOverrides?.description;
  const descriptions: GameDescriptions = manualOverride
    ? { tight: manualOverride, default: manualOverride, loose: manualOverride }
    : (descriptionsBySlug[entry.slug] ?? {
        tight: bgg.description,
        default: bgg.description,
        loose: bgg.description,
      });

  const base = {
    slug: entry.slug,
    bggId: entry.bggId,
    accentHex: entry.accentHex,
    bgg,
    descriptions,
    thumbnail: thumbnailBySlug[entry.slug] ?? placeholderThumbnail,
    title: entry.displayTitle ?? bgg.name,
    ...(entry.family ? { family: entry.family } : {}),
    ...(entry.isNew ? { isNew: true } : {}),
  };

  const playable = playableBySlug.get(entry.slug);
  if (playable) {
    const def: PlayableGame = { ...base, ...playable, kind: "playable" };
    return def;
  }
  const def: CatalogGame = { ...base, kind: "catalog" };
  return def;
}

export const games: GameDefinition[] = catalog
  .map(buildDefinition)
  .sort((a, b) => a.title.localeCompare(b.title));

/**
 * Min/max `averageWeight` across the registry — used by `BggInline` to
 * normalize the complexity bar so the lightest game in the catalog reads
 * as empty and the heaviest reads as full. Recomputed once at module load.
 */
export const weightStats: { min: number; max: number } = (() => {
  const weights = games
    .map((g) => g.bgg.averageWeight)
    .filter((w): w is number => w !== null && w > 0);
  if (weights.length === 0) return { min: 1, max: 5 };
  return { min: Math.min(...weights), max: Math.max(...weights) };
})();
